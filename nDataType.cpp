#include<iostream>
using namespace std;

int main(){
    int a;
    cout << "size of int a " << sizeof(a) << endl;

    float b;
    cout << "size of float b " << sizeof(b) << endl; 

    char c;
    cout << "size of char c " << sizeof(c) << endl;

    bool d;
    cout << "size of bool d " << sizeof(d) << endl;
}